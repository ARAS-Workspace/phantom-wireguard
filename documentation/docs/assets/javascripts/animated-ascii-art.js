/**
 * Animated ASCII Art - Index Page
 * Auto-playing pulse animation for the documentation index.
 */

document.addEventListener('DOMContentLoaded', function() {
    'use strict';

    if (typeof PhantomASCII === 'undefined') {
        console.error('PhantomASCII module not loaded');
        return;
    }

    const pulseElement = document.getElementById('phantom-ascii-pulse');
    if (pulseElement) {
        new PhantomASCII('phantom-ascii-pulse', {
            animationSpeed: 500,
            autoStart: true
        });
    }
});